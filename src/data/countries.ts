export function getCountryByCode(code: string): Country | undefined {
  return COUNTRIES.find((c) => c.code === code)
}

export function getCountriesByContinent(continent: Continent): Country[] {
  return COUNTRIES.filter((c) => c.continent === continent)
}

export function groupByContinent(codes: string[]): Record<Continent, number> {
  const result = {} as Record<Continent, number>
  for (const continent of CONTINENTS) {
    result[continent] = 0
  }
  for (const code of codes) {
    const country = getCountryByCode(code)
    if (country) result[country.continent] = (result[country.continent] || 0) + 1
  }
  return result
}
